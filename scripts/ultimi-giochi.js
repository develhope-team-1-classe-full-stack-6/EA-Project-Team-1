function colGenerator(item, cont) {
  for (let i = 0; i < cont.length; i++) {
    row.innerHTML += `<div class="box col-12 col-md-6 col-xl-4 pb-4">
  ${item(cont[i].src1, cont[i].logoWidth, cont[i].src2, cont[i].href1, cont[i].href2, cont[i].href3)}
  </div>`;
  }
}

